<h2 id="desc">Description du service</h2>
<p>Le site <span class="site">www.ikastolen-pestak.eus</span> permet de consulter les fiches des fêtes contenus dans sa base de
    données. Les fêtes sont décrits à l'aide de caractéristiques précises. Celles-ci permettent d'effectuer des
    recherches précises.
    Les utilisateurs ont la possibilité d'émettre un avis personnel sur les fêtes auxquelles ils ont pu participer.</p>

<p>Les organisateurs des fêtes ont la possibilité de <em>répondre</em> aux critiques reçues sur le site.
    De même les critiques sont relues par une équipe de modération interne à l'association ikastolen-pestak. </p>

<h3  id="fonc">Fonctionnalités utilisateurs</h3>
<h4>Conditions générales</h4>
<p>
    L'inscription et la publication d'avis ou d'une note est réservée aux personnes <strong>majeures</strong>.
</p>
<p>
    Lors de la publication d'un avis l'utilisateur comprend qu'une modération sera effectuée <strong>avant</strong> que
    l'avis soit définitivement visible sur le site <span class="site">www.ikastolen-pestak.eus</span>.
</p>
Sont <strong>exclus</strong> du droit de publication d'avis :
<ul>
    <li>les organisateurs</li>
    <li>les permanents de l'association ikastolen-pestak</li>
</ul>
<p>
    Les utilisateurs doivent porter un avis objectif sur la qualité gastronomique d'une fête.
</p>
Dans les commentaires peuvent être évalués :
<ul>
    <li>la qualité gustative des différentes gastronomies</li>
    <li>la diversité des proposition culinaires</li>
    <li>le coût</li>
    <li>la qualité du service (en se souvenant que les serveurs sont des bénévoles)</li>
</ul>

<p>
    Les jugements personnels autres <strong>ne peuvent</strong> être acceptés.
</p>
<p>
    Toute remarque sur l'origine ethnique, culturelle, l'orientation sexuelle, sera modérée, et le compte utilisateur
    pourra faire l'objet d'une suspension ou d'une suppression.
</p>

